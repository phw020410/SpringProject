create table chemical_element (
id integer primary key not null,
name varchar(20) not null,
symbol varchar(2) not null,
standard_atomic_weight float,
atomic_number integer not null,
chemical_group tinyint not null,
chemical_period tinyint not null,
chemical_series varchar(30),
electron_configuration varchar(255) not null,
electron_per_shell varchar(255) not null,
appearance varchar(20),
chemical_phase varchar(20),
melting_point float,
boiling_point float,
density float,
liquid_density_at_MP float,
liquid_density_at_BP float,
triple_point_temperature float,
triple_point_pressure float,
heat_of_fusion float,
heat_of_vaporization float,
heat_of_capacity float
)